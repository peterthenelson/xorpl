/// Emit a self-contained, unmasked Rust evaluation function for server-side
/// verification.
///
/// The emitted function evaluates F(inputs) plaintext — no POOL, no Beaver
/// triples, no masking.  It is the server's oracle: given the same inputs as
/// the browser, it must produce the same `u32` checksum.
///
/// Intended use: write the output to `generated/verifier.rs` inside a
/// `workers-rs` Cloudflare Worker crate, then `include!` it.  The crate has
/// no dependency on xorpl at runtime.
///
/// Each Remask gadget emits a transparent identity assignment (`let wN = wM`)
/// which the compiler eliminates.  All other gadgets map directly to their
/// unmasked arithmetic.
///
/// Parameters are named `input_{name}` (where `name` is the circuit's Ingest
/// name), avoiding any collision with the `w{wire_id}` namespace used for
/// intermediate variables.
pub fn emit_verifier_rust(circuit: &Circuit, fn_name: &str, expr_digest: &[u8; 32]) -> String {
    let sig_params = circuit.gadgets.iter()
        .filter_map(|g| if let Gadget::Ingest { name, .. } = g { Some(name.as_str()) } else { None })
        .map(|name| format!("input_{name}: u32"))
        .collect::<Vec<_>>()
        .join(", ");

    let mut out = String::new();
    out.push_str("// Generated by xorpl — do not edit manually.\n");
    out.push_str(&format_digest_const(expr_digest));
    out.push_str(&format!("pub fn {fn_name}({sig_params}) -> u32 {{\n"));

    for g in &circuit.gadgets {
        out.push_str(&emit_plain_gadget(g));
    }

    out.push_str("}\n");
    out
}

fn emit_plain_gadget(g: &Gadget) -> String {
    match g {
        Gadget::Ingest { name, out, .. } =>
            format!("    let w{out} = input_{name};\n"),
        Gadget::PublicConst { k, out } =>
            format!("    let w{out} = 0x{k:08x}u32;\n"),
        Gadget::SecretConst { k, out, .. } =>
            format!("    let w{out} = 0x{k:08x}u32;\n"),
        Gadget::Xor { a, b, out } =>
            format!("    let w{out} = w{a} ^ w{b};\n"),
        Gadget::XorConst { a, k, out } =>
            format!("    let w{out} = w{a} ^ 0x{k:08x}u32;\n"),
        Gadget::AndConst { a, k, out } =>
            format!("    let w{out} = w{a} & 0x{k:08x}u32;\n"),
        Gadget::Rotl { a, r, out } =>
            format!("    let w{out} = w{a}.rotate_left({r});\n"),
        Gadget::And { a, b, out, .. } =>
            format!("    let w{out} = w{a} & w{b};\n"),
        Gadget::Remask { a, out, .. } =>
            format!("    let w{out} = w{a};\n"),
        Gadget::Egress { a } =>
            format!("    w{a}\n"),
    }
}

// ---------------------------------------------------------------------------
// Register allocation
// ---------------------------------------------------------------------------

/// Compute the last step (gadget index) at which each wire is read.
/// Wires that are never read after definition have `last_use == def_step`.
fn last_use_steps(circuit: &Circuit) -> Vec<usize> {
    let n = circuit.gadgets.iter()
        .flat_map(|g| g.out().into_iter().chain(g.input_wires()))
        .max()
        .map(|m| m + 1)
        .unwrap_or(0);

    let mut last_use = vec![0usize; n];
    for (step, g) in circuit.gadgets.iter().enumerate() {
        if let Some(out) = g.out() {
            last_use[out] = step;
        }
    }
    for (step, g) in circuit.gadgets.iter().enumerate() {
        for wire in g.input_wires() {
            if step > last_use[wire] {
                last_use[wire] = step;
            }
        }
    }
    last_use
}

/// Linear-scan register allocator with a random slot-index shuffle.
///
/// Returns a `slot` vector indexed by `WireId`; only entries for wires with
/// definitions (those that appear as `g.out()`) are meaningful — others hold
/// `usize::MAX`.
///
/// The shuffle permutes slot indices with `rng` so the `r[k]` values in the
/// emitted code differ between rotations without changing circuit semantics.
fn allocate_registers(circuit: &Circuit, rng: &mut impl rand::RngCore) -> Vec<usize> {
    let last_use = last_use_steps(circuit);
    let n        = last_use.len();
    let n_steps  = circuit.gadgets.len();

    // expire_at[s] = wires whose slot becomes free at the start of step s
    //                (last_use of the wire was s-1).
    let mut expire_at: Vec<Vec<WireId>> = vec![Vec::new(); n_steps + 1];

    let mut slot   = vec![usize::MAX; n];
    let mut free   = Vec::<usize>::new(); // available indices, kept sorted ascending
    let mut n_slots = 0usize;

    for (step, g) in circuit.gadgets.iter().enumerate() {
        // Reclaim slots from wires that died before this step.
        for wire in expire_at[step].drain(..) {
            let s = slot[wire];
            let pos = free.partition_point(|&x| x < s);
            free.insert(pos, s);
        }

        // Assign a slot to this gadget's output, preferring the smallest free slot.
        if let Some(out) = g.out() {
            let s = if !free.is_empty() {
                free.remove(0)
            } else {
                let s = n_slots;
                n_slots += 1;
                s
            };
            slot[out] = s;
            // The slot becomes reclaimable once this wire's last read has passed.
            expire_at[last_use[out] + 1].push(out);
        }
    }

    if n_slots == 0 {
        return slot;
    }

    // Shuffle the slot indices for structural variation across rotations.
    let mut perm: Vec<usize> = (0..n_slots).collect();
    use rand::seq::SliceRandom;
    perm.shuffle(rng);

    for s in slot.iter_mut() {
        if *s != usize::MAX {
            *s = perm[*s];
        }
    }

    slot
}

