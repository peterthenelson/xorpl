#[cfg(test)]
mod tests {
    use super::*;
    use crate::expr::Expr;
    use crate::lower::lower_to_circuit;

    fn or_rotl_circuit() -> Circuit {
        let a = Expr::input("a");
        let b = Expr::input("b");
        let c = Expr::secret_const(0x9e37_79b9);
        let expr = Expr::rotl(Expr::xor(Expr::or(a, b), c), 5);
        lower_to_circuit(&expr)
    }

    #[test]
    fn verifier_has_correct_signature() {
        let circuit  = or_rotl_circuit();
        let emitted  = emit_verifier_rust(&circuit, "my_fn", &[0u8; 32]);
        assert!(emitted.contains("pub fn my_fn(input_a: u32, input_b: u32) -> u32 {"),
            "wrong signature:\n{emitted}");
        assert!(emitted.contains("pub const EXPR_DIGEST"),
            "missing EXPR_DIGEST:\n{emitted}");
    }

    #[test]
    fn verifier_agrees_with_circuit_eval() {
        let circuit = or_rotl_circuit();
        let emitted = emit_verifier_rust(&circuit, "checksum", &[0u8; 32]);

        // Spot-check a few expected lines against what circuit.eval() does.
        // The verifier emits w{id} = <op>; we verify these match the gadget list.
        let inputs: std::collections::HashMap<String, u32> =
            [("a", 0x1234_5678u32), ("b", 0xDEAD_BEEFu32)]
            .iter().map(|&(k, v)| (k.to_string(), v)).collect();
        let vals = circuit.eval(&inputs);
        let expected = vals[&circuit.egress];

        // Cross-check: formula matches known output.
        let known = ((0x1234_5678u32 | 0xDEAD_BEEFu32) ^ 0x9e37_79b9u32).rotate_left(5);
        assert_eq!(expected, known);

        // Structural: ingest wires reference their prefixed parameter names.
        assert!(emitted.contains("let w0 = input_a;"), "missing ingest a:\n{emitted}");
        assert!(emitted.contains("let w1 = input_b;"), "missing ingest b:\n{emitted}");
        assert!(emitted.contains("rotate_left(5)"),  "missing rotl:\n{emitted}");
        assert!(emitted.contains("0x9e3779b9u32"), "missing secret const:\n{emitted}");
    }

    #[test]
    fn verifier_remask_is_transparent() {
        use crate::circuit::Builder;
        let mut builder = Builder::new();
        let wa = builder.ingest("x");
        let wr = builder.remask(wa);
        let circuit = builder.build(wr);
        let emitted = emit_verifier_rust(&circuit, "f", &[0u8; 32]);
        // Remask emits an identity assignment; no POOL reference.
        assert!(emitted.contains("let w1 = w0;"), "remask should be identity:\n{emitted}");
        assert!(!emitted.contains("POOL"), "verifier must not reference POOL:\n{emitted}");
    }

    #[test]
    fn verifier_contains_no_pool_or_beaver() {
        let circuit = or_rotl_circuit();
        let emitted = emit_verifier_rust(&circuit, "f", &[0u8; 32]);
        assert!(!emitted.contains("POOL"),  "verifier must not reference POOL");
        assert!(!emitted.contains("let (t, ma, mb)"), "verifier must not use Beaver triples");
    }
}
